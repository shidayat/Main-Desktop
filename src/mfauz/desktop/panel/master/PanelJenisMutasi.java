    @Override
    public void valueChanged(ListSelectionEvent e) {
          if(e.getValueIsAdjusting()) {
                return;
              }
        if(tblPerson.getSelectedRow() >=0) {
            jm = listJenisMutasi.get(tblPerson.getSelectedRow());
            loadModelToForm();
            enableForm(false);
            selectButton();
        }
    }

    public class JenisMutasiTableModel extends AbstractTableModel{
    private final List<JenisMutasi> listJenisMutasi;
    private final String[] HEADER = new String[] {"KODE", "JENIS MUTASI", "PERIODE", "TERIMA RP","KELUAR RP"};
   
    public JenisMutasiTableModel(List<JenisMutasi> listJenisMut) {
        this.listJenisMutasi = listJenisMut;
    }
    
    
    @Override
    public int getRowCount() {
        return listJenisMutasi.size();   // size untuk tipe List
    }

    @Override
    public int getColumnCount() {
        return HEADER.length;   //length untuk tipe array
    }
    
    @Override
    public String getColumnName(int column){
        return HEADER[column];
    }

    
    @Override
    public Object getValueAt(int rowIndex, int columnIndex) {
      JenisMutasi j = listJenisMutasi.get(rowIndex);
      switch(columnIndex){
          
          case 0:
              return j.getId();
          case 1:
              return j.getNamaMutasi();
          case 2:
              return j.getPeriode();
          case 3:
              return TextComponentUtils.formatNumber(j.getTerimaRp());   
          case 4:
              return TextComponentUtils.formatNumber(j.getKeluarRp());
          default:
              return "";
      }
    }
    
 
    @Override
    public Class<?> getColumnClass(int columnIndex) {
         return String.class;
    }
    }    
    
