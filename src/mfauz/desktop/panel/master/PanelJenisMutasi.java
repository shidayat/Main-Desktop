   private void loadModelToForm() {
      
        txtPeriode.setText(jm.getPeriode());
        txtJenisMutasi.setText(jm.getNamaMutasi());
    }
        
     private void updateTablePerson() {
        listJenisMutasi = Main.getAppService().findAllJenisMutasi();
        if(listJenisMutasi != null){
        tblPerson.setModel(new JenisMutasiTableModel(listJenisMutasi));
        tblPerson.setAutoCreateColumnsFromModel(true);
        tblPerson.getSelectionModel().addListSelectionListener(this);  
        TableUtil.autoResizeColumn(tblPerson);
        }
    }     

