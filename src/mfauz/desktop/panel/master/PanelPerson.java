private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    if(person!=null) {
          int message=JOptionPane.showConfirmDialog(this, "Yakin akan menghapus data person? ==> "
                +person.getPersonId()+" "+person.getNama(),
                "Konfirmasi Hapus Data", JOptionPane.YES_NO_OPTION, 1);
           if(message==0){
            Main.getAppService().deletePerson(person);
            defaultButton();
            clearForm();
            enableForm(false);
            person = null;
            updateTablePerson();
            JOptionPane.showMessageDialog(this,
                    "Data Telah Terhapus!",
                    "Selesai", JOptionPane.INFORMATION_MESSAGE);
           } else {
                defaultButton();
                clearForm();
                enableForm(false);}
    } else {
        JOptionPane.showMessageDialog(this,
                "Tidak ada data yang dipilih!",
                "Kesalahan", JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_btnDeleteActionPerformed

