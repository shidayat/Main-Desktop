private void loadFormToModel(){
//    person = new Person();
    person.setNama(txtNama.getText());
    person.setStatus(String.valueOf(cboStatus.getSelectedItem()));
    person.setKotaSektor(txtKotaSektor.getText());
    person.setKomitmen(new BigDecimal(txtKomitmen.getText()));
    person.setTanggalKomitmen(tglKomitmen.getDate());
    if(tglPindah.getDate() == null){
      person.setTanggalPindah(null);
    } else { 
      person.setTanggalPindah(tglPindah.getDate()); 
    }
    person.setHutangTahunIni(BigDecimal.ZERO);
    person.setHutangTahunLalu(BigDecimal.ZERO);
    person.setKomitmenTahunIni(new BigDecimal(txtKomitmen.getText()));
    person.setPembayaranTahunIni(BigDecimal.ZERO);
    person.setTotalHutang(BigDecimal.ZERO);
    String kataSandi = new MD5(new String(pasAk.getPassword())).asHex();
    person.setPassword(kataSandi);
}

private void simpanAction() {
    
    if(validateForm()){
        if (person==null) {
            person=new Person();
        }
        loadFormToModel();  
        try {
        Main.getAppService().savePerson(person);
        }
        catch (Exception ex) {
             ex.printStackTrace();
          JOptionPane.showMessageDialog(this, "Data gagal disimpan!"
            ,"Error",JOptionPane.ERROR_MESSAGE);
        }
        defaultButton();
        clearForm();
        enableForm(false);
        updateTablePerson();
        JOptionPane.showMessageDialog(this, "Data Person Telah Disimpan!",
                "Konfirmasi", JOptionPane.INFORMATION_MESSAGE);
    }
}

private Boolean validateForm(){
    if(txtNama.getText().equals("") || txtKomitmen.getText().equals("")
          || cboStatus.getSelectedIndex()== -1 || tglKomitmen.getDate() == null) {
        JOptionPane.showMessageDialog(this, 
                "Harap isi semua field Penting!!", "Kesalahan",
                JOptionPane.ERROR_MESSAGE);
        return false;       
    }

   return true;
}

