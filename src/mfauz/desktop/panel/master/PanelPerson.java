   private void loadModelToForm() {
        txtKomitmen.setText(String.valueOf(person.getKomitmen()));
        txtKotaSektor.setText(person.getKotaSektor());
        txtNama.setText(person.getNama());
        tglKomitmen.setDate(person.getTanggalKomitmen());
        tglPindah.setDate(person.getTanggalPindah());
        cboStatus.setSelectedItem(person.getStatus());
        if(person.getPersonId()==0) {
            txtKomitmen.setText("");
        }
    }
        
     private void updateTablePerson() {
        listPerson = Main.getAppService().findAllPerson();
        if(listPerson != null){
        tblPerson.setModel(new PersonTableModel(listPerson));
        tblPerson.setAutoCreateColumnsFromModel(true);
        tblPerson.getSelectionModel().addListSelectionListener(this);  
        TableUtil.autoResizeColumn(tblPerson);
        }
    }     

